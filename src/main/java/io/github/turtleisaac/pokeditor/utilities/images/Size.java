package io.github.turtleisaac.pokeditor.utilities.images;

public class Size
{

    public int width;
    public int height;

    public Size()
    {
        width= 0;
        height= 0;
    }

    public Size(int width, int height)
    {
        this.width= width;
        this.height= height;
    }
}
